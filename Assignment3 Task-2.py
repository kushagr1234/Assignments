num=int(input("Enter a number:\t"))

#CALCULATING SQUARE ROOT=>

import math


sqr= math.sqrt(num)
print("The square root of",num,"is:\t",sqr)



#CALCULATING LOG=>


log=math.log(num)
print("The log of",num,"is:\t",log)


#CALCULATING SINE(IN RADIANS)


sine=math.sin(num)
print("The sine of",num,"is:\t",sine)

